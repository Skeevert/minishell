#include <string.h>

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t		i;
	size_t		max;

	if (!s1 || !s2)
		return (0);
	i = 0;
	max = n;
	while (i < max && (s1[i] || s2[i]))
	{
		if (s1[i] != s2[i])
			return (0);
		i++;
	}
	return (1);
}
